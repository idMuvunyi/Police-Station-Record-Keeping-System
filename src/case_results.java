    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
         if(caseno.getText().trim().isEmpty() && casenid.getText().trim().isEmpty() ){
            JOptionPane.showMessageDialog(null,"Fields are empty");
        }

        else if(caseno.getText().trim().isEmpty() ){
            JOptionPane.showMessageDialog(null,"add serial number");
        }
        else if(casenid.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Fill out NID of the victim");
        }
        
        

        else{

            int sr =Integer.parseInt(caseno.getText());
            String nid =casenid.getText();
            
            String result = resultsarea.getText();
            Date d = new Date();
            SimpleDateFormat s =new SimpleDateFormat("d-MM-Y");
            String dates = s.format(d);

            try{
                Connection Con=DriverManager.getConnection("jdbc:mysql://localhost/police","root","");
                PreparedStatement st = Con.prepareStatement("INSERT INTO results VALUES(?,?,?,?)");

                st.setInt(1,sr);
                st.setString(2,nid);
                st.setString(3,result);
                st.setString(4,dates);
                
                st.execute();

                JOptionPane.showMessageDialog(null,"New Case result Added");
                caseno.setText("");
                casenid.setText("");
                resultsarea.setText("");
                caseno.requestFocus();

            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
         fetchData_Station();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tblresultsMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblresultsMousePressed
        // TODO add your handling code here:
        
          
        int i = tblresults.getSelectedRow();
        TableModel model = tblresults.getModel();
        String msg_Id = (model.getValueAt(i,0).toString());
       
        try{
                Connection Con=DriverManager.getConnection("jdbc:mysql://localhost/police","root","");
                PreparedStatement m=Con.prepareStatement("SELECT * FROM results WHERE id=?");
                m.setString(1,msg_Id);

                ResultSet result =m.executeQuery();
               
                if(result.next()){
               
                   
                   String stm = result.getString("result_case");
                   NewClass.names = stm;
                   resultsarea.setLineWrap(true);
                   resultsarea.setWrapStyleWord(true);
                   resultsarea.setText(String.valueOf(NewClass.names).toString());
                   
                   
                   
                }

            }
            catch(Exception ex){
                ex.printStackTrace();
            }
    }//GEN-LAST:event_tblresultsMousePressed

     void fetchData_Station() {
        
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection Con=DriverManager.getConnection("jdbc:mysql://localhost/police","root","");
                
                Statement st = Con.createStatement();
                ResultSet rs = st.executeQuery("SELECT id,nid,date FROM results");
                while(tblresults.getRowCount() > 0){
                    ((DefaultTableModel)tblresults.getModel()).removeRow(0);
                   
                    
                }
                
                 int col = rs.getMetaData().getColumnCount();
                 while(rs.next()){
                     Object [] rows = new Object[col];
                     for(int l = 1; l <= col; l++ )
                     {
                        rows[l-1] = rs.getObject(l);
                     }
                     ((DefaultTableModel)tblresults.getModel()).insertRow(rs.getRow() -1, rows);
                 }
                rs.close();
                st.close();
                
            } 
            catch (Exception e) {
                e.printStackTrace();
            }
       
    }
    
    
