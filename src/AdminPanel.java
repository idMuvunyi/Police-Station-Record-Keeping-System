   void fetchData_Admin() {
        
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection Con=DriverManager.getConnection("jdbc:mysql://localhost/police","root","");
                
                Statement st = Con.createStatement();
                ResultSet rs = st.executeQuery("SELECT * FROM admin");
                while(table_Admin.getRowCount() > 0){
                    ((DefaultTableModel)table_Admin.getModel()).removeRow(0);
                   
                    
                }
                
                 int col = rs.getMetaData().getColumnCount();
                 while(rs.next()){
                     Object [] rows = new Object[col];
                     for(int l = 1; l <= col; l++ )
                     {
                        rows[l-1] = rs.getObject(l);
                     }
                     ((DefaultTableModel)table_Admin.getModel()).insertRow(rs.getRow() -1, rows);
                 }
                rs.close();
                st.close();
                
            } 
            catch (Exception e) {
                e.printStackTrace();
            }
            
        
    }
   
   
